scrollToTopBtn.addEventListener('mouseleave', function() {
    this.style.transform = 'scale(1)';
    this.style.background = '#ff6b9d';
});

// Fullscreen Modal Functionality
document.addEventListener('DOMContentLoaded', function() {
    const modal = document.getElementById('imageModal');
    const modalImg = document.getElementById('modalImage');
    const modalCaption = document.getElementById('modalCaption');
    const closeBtn = document.getElementsByClassName('close')[0];

// Get all member and album images
const memberImages = document.querySelectorAll('.member-photo');
const albumImages = document.querySelectorAll('.album-image');

console.log('Member images found:', memberImages.length);
console.log('Album images found:', albumImages.length);

// Add click event listeners to member images
memberImages.forEach((img, index) => {
    img.addEventListener('click', function() {
        // Remove all existing overlay classes
        modal.className = 'modal';
        
        // Add appropriate member overlay class based on index
        const memberOverlays = [
            'heejin-overlay', 'hyunjin-overlay', 'haseul-overlay', 'yeojin-overlay',
            'vivi-overlay', 'kimlip-overlay', 'jinsoul-overlay', 'choerry-overlay',
            'yves-overlay', 'chuu-overlay', 'gowon-overlay', 'oliviahye-overlay'
        ];
        
        if (memberOverlays[index]) {
            modal.classList.add(memberOverlays[index]);
        }
        
        modal.style.display = 'block';
        modalImg.src = this.src;
        modalImg.alt = this.alt;
        
        // Prevent body scroll when modal is open
        document.body.style.overflow = 'hidden';
    });
});

// Add click event listeners to album images
albumImages.forEach(img => {
    img.addEventListener('click', function() {
        // Remove all existing overlay classes and add album overlay
        modal.className = 'modal album-overlay';
        
        modal.style.display = 'block';
        modalImg.src = this.src;
        modalImg.alt = this.alt;
        
        // Prevent body scroll when modal is open
        document.body.style.overflow = 'hidden';
    });
});

// Close modal when clicking the X button
closeBtn.addEventListener('click', function() {
    modal.style.display = 'none';
    modal.className = 'modal'; // Reset overlay class
    document.body.style.overflow = 'auto';
});

// Close modal when clicking outside the image
modal.addEventListener('click', function(e) {
    if (e.target === modal) {
        modal.style.display = 'none';
        modal.className = 'modal'; // Reset overlay class
        document.body.style.overflow = 'auto';
    }
});

// Close modal with Escape key
document.addEventListener('keydown', function(e) {
    if (e.key === 'Escape' && modal.style.display === 'block') {
        modal.style.display = 'none';
        modal.className = 'modal'; // Reset overlay class
        document.body.style.overflow = 'auto';
    }
});

}); // End of DOMContentLoaded

